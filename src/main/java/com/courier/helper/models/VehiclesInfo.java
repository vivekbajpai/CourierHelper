package com.courier.helper.models;

public class VehiclesInfo {
	private int count;
	private int capacityWeight;
	private int speed;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getCapacityWeight() {
		return capacityWeight;
	}
	public void setCapacityWeight(int maxWeight) {
		this.capacityWeight = maxWeight;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	

}
